class IfE {
    public static void main(String[] args) {
        int x = 10;
        if (x > 5) {
            System.out.println(1);
        }
        if (x > 9) {
            System.out.println(1);
        } else {
            System.out.println(0);
        }

        if (x < 15) {
            System.out.println(1);
        } else {
            System.out.println(0);
        }
    }
}